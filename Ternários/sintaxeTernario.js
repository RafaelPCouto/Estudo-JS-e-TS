/**
 * condição ? 'faça algo' : 'faça outra coisa';
 * 
 * Comparando a uma estrutura com if else, o ternário é mais enxuto
 * 
 * A '?' representa o 'if', é como se estivéssemos fazendo uma pergunta de verificação da condição apresentada anteriormente. Se cumprida, o comando a seguir é orientado.
 * Em seguida, o ':' seria a representação de uma alternativa. Se a condição anterior não for atendida, execute o próximo comando.
 * 
 * Lembrando que o Ternário requer cuidado, por ele pode se tornar mais ilegível.
 * Não é positivo, por exemplo, utilizar ternário para implementar muitas condições.
 * A idéia é, justamente, ter uma sintaxe mair curta e legível, evitando encadear condições.
 */